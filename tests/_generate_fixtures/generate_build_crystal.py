# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------

def main():
    if not Path(ATOMSK_BIN).exists():
        raise SystemExit(f"atomsk binary not found at {ATOMSK_BIN}")

    print(f"Writing fixtures to {OUT_DIR}")

    with tempfile.TemporaryDirectory() as td:
        td = Path(td)

        # Plain (non-Miller) catalog
        for fixture_name, atomsk_struct, lattice_args, species, _kw, _names in CATALOG:
            out = td / f"{fixture_name}.lmp"
            _run_atomsk(atomsk_struct, lattice_args, species, out)
            box, pos, ele = _read_canonical(out)
            _save_fixture(fixture_name, box, pos, ele)
            print(f"  ✓ {fixture_name}: {len(pos)} atoms")

        # Miller catalog
        for (fixture_name, atomsk_struct, lattice_args, species,
             miller, _kw, _names) in MILLER_CATALOG:
            out = td / f"{fixture_name}.lmp"
            cmd = [ATOMSK_BIN, "--create", atomsk_struct,
                   *[str(x) for x in lattice_args],
                   *species,
                   "orient", *miller,
                   str(out), "lammps"]
            res = subprocess.run(cmd, capture_output=True, text=True)
            if res.returncode != 0:
                raise RuntimeError(
                    f"atomsk failed:\n{' '.join(cmd)}\n{res.stderr}"
                )
            box, pos, ele = _read_canonical(out)
            _save_fixture(fixture_name, box, pos, ele)
            print(f"  ✓ {fixture_name} (miller): {len(pos)} atoms")


if __name__ == "__main__":
    main()
