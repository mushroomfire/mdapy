"""
Reference fixtures for ``build_crystal``: every structure mdapy supports is
also generated by atomsk's ``--create`` command and the canonical
(box, atoms, elements) tuple is committed under
``tests/fixtures/build_crystal/``. The actual test
(``tests/test_build_crystal.py``) does not need atomsk at runtime.

Run manually whenever a structure definition changes (or atomsk is
upgraded):

    python tests/_generate_fixtures/generate_build_crystal.py

Requires:
    - atomsk binary at ATOMSK_BIN
    - mdapy installed (only used to read the produced LAMMPS data files)
"""

from __future__ import annotations

import os
import subprocess
import sys
import tempfile
from pathlib import Path

import numpy as np

HERE = Path(__file__).parent
sys.path.insert(0, str(HERE))

import mdapy as mp                                              # noqa: E402

ATOMSK_BIN = os.environ.get(
    "ATOMSK_BIN",
    "/u/22/wuy33/unix/Study/atomsk/src/atomsk",
)
OUT_DIR = HERE.parent / "fixtures" / "build_crystal"
OUT_DIR.mkdir(parents=True, exist_ok=True)


def _run_atomsk(structure: str, lattice_args, species, outfile: Path):
    """Invoke atomsk to write a LAMMPS data file. ``lattice_args`` is a
    list of strings appended after ``--create <structure>`` — typically
    ``[a]`` for cubic or ``[a, c]`` for hexagonal/tetragonal."""
    cmd = [ATOMSK_BIN, "--create", structure,
           *[str(x) for x in lattice_args],
           *species,
           str(outfile), "lammps"]
    res = subprocess.run(cmd, capture_output=True, text=True)
    if res.returncode != 0:
        raise RuntimeError(
            f"atomsk failed: {' '.join(cmd)}\nstdout:\n{res.stdout}\n"
            f"stderr:\n{res.stderr}"
        )


def _read_canonical(lmp_path: Path):
    """Read a LAMMPS data file and return a canonical
    ``(box, sorted_positions, sorted_elements)`` triple — positions are
    rounded to 6 decimals before lex-sorting so tiny numerical noise
    (~1e-16) cannot flip rows that share an axis value with one
    implementation and not the other."""
    s = mp.System(str(lmp_path))
    pos = s.data.select("x", "y", "z").to_numpy()
    elements = s.data["element"].to_list() if "element" in s.data.columns \
               else [None] * s.N
    # Round to 6 decimals for sort key only; original positions are kept.
    key = np.round(pos, 6)
    order = np.lexsort((key[:, 2], key[:, 1], key[:, 0]))
    return s.box.box.copy(), pos[order], [elements[i] for i in order]


def _save_fixture(name: str, box, positions, elements):
    """Persist a canonical reference for one structure call."""
    np.savez_compressed(
        OUT_DIR / f"{name}.npz",
        box=np.asarray(box, dtype=np.float64),
        positions=np.asarray(positions, dtype=np.float64),
        elements=np.asarray(elements, dtype="<U4"),
    )


